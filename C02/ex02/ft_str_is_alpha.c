int	ft_str_is_alpha(char *str)
{
	int	i;

	i = 1;
	if (*str == '\0')
		return (1);
	while (*str != '\0')
	{
		if ((*str < 97 || *str > 122) && (*str < 65 || *str > 90))
			return (0);
		str++;
	}
	return (i);
}
