int	ft_str_is_numeric(char *str)
{
	int	i;

	i = 1;
	if (*str == '\0')
		return (1);
	while (*str != '\0')
	{
		if (*str < 48 || *str > 57)
			return (0);
		str++;
	}
	return (i);
}
